const regExp = new RegExp(Object.keys(emojis).map(k => {
  return k = k.replace(':', '\\:')
              .replace(')', '\\)')
              .replace(']', '\\]')
              .replace('[', '\\[')
              .replace('-', '\\-')
              .replace('=', '\\=')
              .replace('(', '\\(')
              .replace('*', '\\*')
              .replace('>', '\\>')
              .replace('<', '\\<')
              .replace('|', '\\|')
  }).join('|') + '\s+$', 'g');

function emojify(string) {
  return string.replace(regExp, (matched) => emojis[matched] ? emojis[matched] : false);
}

